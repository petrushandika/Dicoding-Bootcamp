// Undefined = tipe data terbentuk ketika variabel tidak memiliki sebuah nilai
let score;
console.log(typeof score);

// Number = bilangan bulat dan bilangan desimal
let age = 24;
console.log(typeof age);

let height = 171.5;
console.log(typeof height);

// Aritmatika di dalam number (+, -, /, *, dan %)
let a = 7;
let b = 5;

console.log(a + b);
console.log(a - b);
console.log(a / b);
console.log(a * b);
console.log(a % b);

// Increment (tambah) dan Decrement (kurang)
let rating = 5;
rating++;
console.log(rating);

rating--;
console.log(rating);

// String
let anotherName = "Dicoding Indonesia";
anotherName = "Academy Dicoding";
console.log(anotherName);

// Tanda kutip bersamaan
const question = '"What do you think of JavaScript?" he asked';
console.log(question);

// Escape String(\)
const answer = '"I think it\'s great" i answered';
console.log(answer);

// Boolean = true & false
let x = true;
let y = false;
console.log(typeof x);
console.log(typeof y);

// Null = nilai kosong
let someLaterData = null;
console.log(someLaterData);
